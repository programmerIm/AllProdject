public class Calculator {

    /**
     * 传入两个参数，求和
     * @param a
     * @param b
     * @return
     */
    public int add(int a,int b){

        return a+b;
    }

    /**
     * 传入两个参数，求差
     * @param a
     * @param b
     * @return
     */
    public int sub(int a,int b){

        return a-b;
    }

}